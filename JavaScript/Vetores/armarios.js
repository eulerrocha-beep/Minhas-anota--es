let pokemon = ["pikachu" , "charmander" , "Bulbasaur" , "squirtle"];
//                 0            1              2             3
// vetores ou armarios guardam vários valores em uma única variável 

pokemon.pop(); // remove o último elemento do vetor
pokemon.push("charizard"); // adiciona um elemento no final do vetor
pokemon.shift(); // remove o primeiro elemento do vetor
pokemon.unshift("pikachu"); // adiciona um elemento no início do vetor


console.log(pokemon[0]); //pikachu
console.log(pokemon[2]); //Bulbasaur
console.log(pokemon[1]); //charmander
console.log(pokemon[3]); //squirtle


