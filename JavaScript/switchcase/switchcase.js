// switch case estrutura de decisao

let fruta = "laranja";

switch (fruta) {
    case "laranja":
        case "limao":
            case "tangerina":
        console.log("Fazer uma vitamina");
        break;

    case "banana":
        console.log("Fazer um bolo de banana");
        break;

    case "maca":
        console.log("Fazer uma torta de maca");
        break;

    default:
        console.log("Desculpe, no momento nao temos " + fruta + " disponivel");
}